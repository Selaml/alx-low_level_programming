#include <stdio.h>
#include <ctype.h>


/**
 * main - converts to lower case
 *
 * Return: Always 0 (Success)
 */
int main(void)
{ 
	putchar(tolower('A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J', 'K', 'L', 'M'));
	putchar(tolower('N', 'O', 'P', 'Q', 'R', 'S', 'T', 'U', 'V', 'W', 'X', 'Y', 'Z'));
	return (0);
}
